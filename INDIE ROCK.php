      <p align="justify" class="Estilo5" data-start="1507" data-end="1558"> 3. Explosi&oacute;n del indie nacional (2010s)</p>
      <p align="justify" class="Estilo4" data-start="1559" data-end="1673">Esta d&eacute;cada vio el boom del indie mexicano, con m&aacute;s visibilidad en medios, festivales y plataformas digitales.</p>
      <p align="justify" class="Estilo5" data-start="1675" data-end="1692">Bandas:      </p>
