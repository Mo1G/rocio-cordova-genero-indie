      <p class="Estilo14">-DEFINICION</p>
      <p align="justify">El indie es un g&eacute;nero musical surgido a finales de la d&eacute;cada de 1980 y popularizado a mediados de los noventa.</p>
    <p class="Estilo14">-ORIGEN DEL TERMINO</p>
    <p align="justify">El t&eacute;rmino <strong>indie</strong> proviene de la palabra inglesa &ldquo;<strong>independent</strong>&rdquo;, es decir, independiente. Este estilo comenz&oacute; siendo una manera de hacer m&uacute;sica, para acabar evolucionando en un g&eacute;nero propi<span class="Estilo4">o</span> que engloba diferentes subg&eacute;neros, y de esos subg&eacute;neros m&aacute;s subg&eacute;neros, y m&aacute;s subg&eacute;neros de subg&eacute;neros y as&iacute; hasta acabar hoy. </p>
    <p align="justify"><strong>- CARACTERISTICAS SONORAS</strong></p>
    <p class="Estilo5" data-start="245" data-end="280">1. Producci&oacute;n DIY o &ldquo;Lo-Fi&rdquo;</p>
    <ul class="Estilo4" data-start="281" data-end="483">
      <li data-start="281" data-end="330">
        <div align="justify">Sonido crudo, casero o poco pulido a prop&oacute;sito.</div>
      </li>
      <li>
        <div align="justify">Muchos discos indie fueron grabados con equipos b&aacute;sicos, fuera de grandes estudios.</div>
      </li>
      <li data-start="417" data-end="483">
        <div align="justify">Est&eacute;tica que valora la autenticidad sobre la perfecci&oacute;n t&eacute;cnica.</div>
      </li>
    </ul>
    <p class="Estilo5" data-start="485" data-end="529">2. Guitarras prominentes y creativas</p>
    <ul class="Estilo4" data-start="530" data-end="695">
      <li data-start="530" data-end="602">
        <div align="justify">Uso de guitarras el&eacute;ctricas con efectos (delay, reverb, fuzz, chorus).</div>
      </li>
      <li data-start="603" data-end="652">
        <div align="justify">Riffs mel&oacute;dicos o l&iacute;neas r&iacute;tmicas minimalistas.</div>
      </li>
      <li data-start="653" data-end="695">
        <div align="justify">A menudo m&aacute;s atmosf&eacute;ricas que agresivas.</div>
      </li>
    </ul>
    <p class="Estilo5" data-start="697" data-end="727">3. Ritmos alternativos</p>
    <ul class="Estilo4" data-start="728" data-end="921">
      <li data-start="728" data-end="787">
        <div align="justify">Bater&iacute;a que puede ser tanto muy simple como experimental.</div>
      </li>
      <li data-start="788" data-end="876">
        <div align="justify">Patr&oacute;n r&iacute;tmico no siempre tradicional (mezcla de compases, silencios, o cortes secos).</div>
      </li>
      <li data-start="877" data-end="921">
        <div align="justify">Influencia del post-punk y el garage rock.</div>
      </li>
    </ul>
    <p class="Estilo5" data-start="923" data-end="959">4. Voces con identidad &uacute;nica      </p>
    <ul class="Estilo4" data-start="960" data-end="1114"><li>
      <div align="justify">Voces a veces fr&aacute;giles, nasalizadas o no t&eacute;cnicas. </div>
    </li>
      <li>
        <div align="justify">Expresividad emocional m&aacute;s que potencia vocal.</div>
      </li>
      <li data-start="1062" data-end="1114">
        <div align="justify">Letras introspectivas, existenciales o narrativas.</div>
      </li>
    </ul>
    <p class="Estilo5" data-start="1116" data-end="1154">5. Uso experimental del sonido      </p>
    <ul class="Estilo4" data-start="1155" data-end="1372"><li>
      <div align="justify">Incorporaci&oacute;n de instrumentos no tradicionales: sintetizadores, teclados, cuerdas, cajas de ritmos. </div>
    </li>
      <li>
        <div align="justify">Mezcla de g&eacute;neros (folk, electr&oacute;nica, jazz, psicodelia, punk).</div>
      </li>
      <li data-start="1322" data-end="1372">
        <div align="justify">Ruido y silencio usados con intenci&oacute;n art&iacute;stica.</div>
      </li>
    </ul>
    <p class="Estilo5" data-start="1374" data-end="1402">6. Cambios din&aacute;micos</p>
    <ul class="Estilo4" data-start="1403" data-end="1557">
      <li data-start="1403" data-end="1474">
        <div align="justify">Alternancia entre secciones suaves y explosivas (&quot;quiet-loud-quiet&quot;).</div>
      </li>
      <li data-start="1475" data-end="1557">
        <div align="justify">Canciones que construyen atm&oacute;sfera m&aacute;s que seguir estructuras pop tradicionales.</div>
      </li>
    </ul>
    <p class="Estilo5" data-start="1559" data-end="1593">7. Minimalismo o atm&oacute;sfera</p>
    <ul class="Estilo4" data-start="1594" data-end="1755">
      <li data-start="1594" data-end="1699">
        <div align="justify">En algunas vertientes (como el indie folk o dream pop), se busca un sonido ambiental, relajado, et&eacute;reo.</div>
      </li>
      <li data-start="1700" data-end="1755">
        <div align="justify">Uso de reverb y capas sonoras para crear profundidad.</div>
      </li>
    </ul>    
    <p class="Estilo5" data-start="250" data-end="286">-Influencias del Indie Rock</p>
    <p align="justify" class="Estilo5" data-start="293" data-end="339"> 1. Punk Rock (d&eacute;cadas de 1970&ndash;1980)</p>
    <ul class="Estilo4" data-start="340" data-end="563">
      <li data-start="340" data-end="398">The Clash, Sex Pistols, Buzzcocks</li>
      </ul>
    <p align="justify" class="Estilo5" data-start="570" data-end="593"> 2. Post-Punk</p>
    <ul class="Estilo4" data-start="594" data-end="821"><li data-start="594" data-end="656"><p data-start="596" data-end="656">Joy Division, The Cure, Talking Heads</p>
      </li>
      </ul>
    <p align="justify" class="Estilo5" data-start="828" data-end="861"> 3. New Wave y Synthpop</p>
    <ul class="Estilo4" data-start="862" data-end="1079">
      <li data-start="862" data-end="919">The Smiths, Depeche Mode, R.E.M.</li>
      </ul>
    <p align="justify" class="Estilo5" data-start="1086" data-end="1130"> 4. Garage Rock / Rock Psicod&eacute;lico</p>
    <ul class="Estilo4" data-start="1131" data-end="1291"><li data-start="1131" data-end="1197"><p data-start="1133" data-end="1197">The Velvet Underground, The Stooges, Love</p>
      </li>
      </ul>
    <p align="justify" class="Estilo5" data-start="1298" data-end="1330"> 5. Folk y Cantautores</p>
    <ul class="Estilo4" data-start="1331" data-end="1492"><li data-start="1331" data-end="1394">
      <p data-start="1333" data-end="1394"> Nick Drake, Bob Dylan, Elliott Smith</p>
      </li>
      </ul>
    <p align="justify" class="Estilo5" data-start="1499" data-end="1545"> 6. M&uacute;sica electr&oacute;nica independiente</p>
    <ul class="Estilo4" data-start="1546" data-end="1714">
      <li data-start="1546" data-end="1611"> Radiohead (&eacute;poca Kid A), The Notwist</li>
      </ul>
    <p align="justify" class="Estilo5" data-start="1721" data-end="1760"> 7. Cultura DIY y underground</p>
    <ul class="Estilo4" data-start="1761" data-end="1986"><li data-start="1903" data-end="1986">
        <p align="justify" data-start="1905" data-end="1986">Inspiraci&oacute;n en sellos como Sub Pop, 4AD, Domino Records, Matador.</p>
      </li>
    </ul>
    <p class="Estilo5" data-start="1993" data-end="2040"> Otras influencias menores pero presentes:</p>
    <ul class="Estilo4" data-start="2041" data-end="2282">
      <li data-start="2041" data-end="2128">
        <div align="justify"><strong>Shoegaze: </strong>(My Bloody Valentine) por sus capas de sonido y guitarras texturizadas.</div>
      </li>
      <li data-start="2129" data-end="2194">
        <div align="justify"><strong>Jazz y lo-fi:</strong> en subg&eacute;neros m&aacute;s relajados o experimentales.</div>
      </li>
      <li data-start="2195" data-end="2282">
        <div align="justify"><strong>Pop brit&aacute;nico alternativo:</strong> (Blur, Pulp) especialmente en el indie rock brit&aacute;nico.</div>
      </li>
    </ul>    
    <p align="justify" class="Estilo4"><img src="DISC.jpg" width="192" height="254" /></p></td>
    <td bgcolor="#9999FF" class="Estilo13"><p class="Estilo5">-DECADAS CLAVE</p>
      <p class="Estilo5" data-start="212" data-end="251">1980s: El origen del indie rock</p>
      <ul class="Estilo4" data-start="252" data-end="669">
        <li data-start="252" data-end="343">
          <div align="justify">Nace como una contracultura al mainstream; fuerte &eacute;tica DIY (hazlo t&uacute; mismo).</div>
        </li>
        <li data-start="344" data-end="387">
          <div align="justify">Estados Unidos y Reino Unido.</div>
        </li>
      </ul>
      <p class="Estilo5" data-start="676" data-end="722">1990s: Consolidaci&oacute;n y diversificaci&oacute;n</p>
      <ul class="Estilo4" data-start="723" data-end="1231">
        <li data-start="723" data-end="797">
          <div align="justify">El indie se mezcla con el grunge, el britpop y el post-rock.</div>
        </li>
        <li data-start="798" data-end="871">
          <div align="justify">Alternancia entre el &eacute;xito comercial y el estatus de culto.</div>
        </li>
      </ul>
      <p class="Estilo5" data-start="1238" data-end="1275">2000s: El boom del indie rock</p>
      <ul class="Estilo4" data-start="1276" data-end="1756">
        <li data-start="1276" data-end="1362">
          <div align="justify">&Eacute;poca dorada de popularidad global gracias a internet y blogs musicales.</div>
        </li>
      </ul>
      <p class="Estilo5" data-start="1763" data-end="1819">2010s: Expansi&oacute;n, hibridaci&oacute;n y comercializaci&oacute;n</p>
      <ul class="Estilo4" data-start="1820" data-end="2267">
        <li data-start="1820" data-end="1928">
          <div align="justify">El indie deja de ser &quot;<strong>independiente</strong>&quot; en el sentido cl&aacute;sico; muchos artistas firman con majors.</div>
        </li>
        <li data-start="2120" data-end="2184">
          <div align="justify"><strong>Sonido</strong>: M&aacute;s pulido, mezcla con electr&oacute;nica, R&amp;B y hip hop.</div>
        </li>
        <li>
          <div align="justify"><strong>Fen&oacute;meno</strong>: Festivales como Coachella o Lollapalooza llenos de bandas &quot;indie&quot;.</div>
        </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="2274" data-end="2330">2020s: El indie como actitud m&aacute;s que como g&eacute;nero</p>
      <ul data-start="2331" data-end="2693">
        <li class="Estilo4" data-start="2331" data-end="2423">
          <div align="justify">Lo indie trasciende el formato tradicional de banda; aparece el &quot;<strong>bedroom pop</strong>&quot;.</div>
        </li>
      </ul>
      <p><strong>-HITOS IMPORTANTES</strong></p>
      <p align="justify" class="Estilo5" data-start="154" data-end="245">1. Nacimiento del t&eacute;rmino &quot;indie&quot; (finales de los 70s - principios de los 80s)</p>
      <ul class="Estilo4" data-start="246" data-end="518"><li data-start="382" data-end="518">
        <div align="justify">Fundaci&oacute;n de sellos como Rough Trade (UK) y Sub Pop (EE.UU.), que jugaron un papel crucial en la escena alternativa.</div>
      </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="525" data-end="570"> 2. El debut de The Smiths (1984)</p>
      <ul class="Estilo4" data-start="571" data-end="790">
        <li data-start="571" data-end="722">
          <div align="justify">Con su &aacute;lbum &ldquo;The Smiths&rdquo;, esta banda defini&oacute; el sonido del indie brit&aacute;nico: letras introspectivas, melod&iacute;as melanc&oacute;licas y est&eacute;tica alternativa.</div>
        </li>
        <li>
          <div align="justify">Influenci&oacute; a incontables bandas en UK y fuera de &eacute;l.</div>
        </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="797" data-end="883">3. Surgimiento del indie americano: R.E.M., Pixies, Sonic Youth (a&ntilde;os 80)</p>
      <ul class="Estilo4" data-start="884" data-end="1118">
        <li data-start="884" data-end="979">
          <div align="justify">Bandas como R.E.M. comenzaron a tener &eacute;xito masivo sin comprometer su sonido alternativo.</div>
        </li>
        <li data-start="980" data-end="1118">
          <div align="justify">Pixies (con &quot;Surfer Rosa&quot;, 1988) introdujeron una din&aacute;mica de &ldquo;calma-ruido-calma&rdquo; que influenciar&iacute;a al grunge y al rock alternativo.</div>
        </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="1125" data-end="1183"> 4. &ldquo;Slanted and Enchanted&rdquo; de Pavement (1992)</p>
      <ul class="Estilo4" data-start="1184" data-end="1333"><li data-start="1241" data-end="1333">
        <div align="justify">Marca el inicio del indie de los 90: desenfadado, con guitarras sucias y letras cr&iacute;pticas.</div>
      </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="1340" data-end="1420"> 5. El estallido del indie garage: The Strokes y el &ldquo;revival&rdquo; (2001)</p>
      <ul class="Estilo4" data-start="1421" data-end="1638">
        <li data-start="1421" data-end="1515">
          <div align="justify">Con &ldquo;Is This It&rdquo;, The Strokes redefinen el indie como algo cool, accesible y global.</div>
        </li>
        <li data-start="1516" data-end="1638">
          <div align="justify">Junto a Interpol, The White Stripes y Yeah Yeah Yeahs, marcan el regreso del rock de guitarras con est&eacute;tica moderna.</div>
        </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="1645" data-end="1717"> 6. El debut de Arctic Monkeys y el poder de internet (2006)</p>
      <ul class="Estilo4" data-start="1718" data-end="1963">
        <li data-start="1718" data-end="1838">
          <div align="justify">Su &aacute;lbum &ldquo;Whatever People Say I Am&hellip;&rdquo; se convierte en el m&aacute;s vendido en la historia brit&aacute;nica para una banda debut.</div>
        </li>
        <li data-start="1839" data-end="1963">
          <div align="justify">Fueron una de las primeras bandas descubiertas a trav&eacute;s de MySpace, marcando la importancia de internet para el indie.</div>
        </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="1970" data-end="2035"> 7. Arcade Fire gana el Grammy a &Aacute;lbum del A&ntilde;o (2011)</p>
      <ul class="Estilo4" data-start="2036" data-end="2198">
        <li data-start="2036" data-end="2110">
          <div align="justify">&ldquo;The Suburbs&rdquo; (2010) les da el Grammy, sorprendiendo a la industria.</div>
        </li>
        <li data-start="2111" data-end="2198">
          <div align="justify">Simboliza el momento en que el indie entra al mainstream sin perder autenticidad.</div>
        </li>
      </ul>
      <p align="justify" class="Estilo5" data-start="2205" data-end="2286"> 8. Indie pop y bedroom pop: democratizaci&oacute;n total (finales de 2010s)</p>
      <div align="justify">
        <ul class="Estilo4" data-start="2287" data-end="2511">
          <li data-start="2287" data-end="2401">Artistas como Clairo, Rex Orange County y Girl in Red graban desde sus cuartos y se vuelven virales.</li>
          <li data-start="2402" data-end="2511">Muestra c&oacute;mo lo indie ya no es solo un estilo musical, sino un modelo de producci&oacute;n y actitud creativa.</li>
        </ul>
      </div>
      <p align="justify" class="Estilo5" data-start="2518" data-end="2574"> 9. Lo indie se vuelve una est&eacute;tica (2020s)</p>
      <ul class="Estilo4" data-start="2575" data-end="2827">
        <li data-start="2575" data-end="2720">
          <div align="justify">Lo &quot;indie&quot; ya no se define por si una banda es independiente, sino por su est&eacute;tica, actitud y sonido alternativo al mainstream tradicional.</div>
        </li>
      </ul>
      <p align="justify"><strong>-MOVIMIENTOS  EN MEXICO  </strong></p>
      <p align="justify" class="Estilo5" data-start="303" data-end="364">1. Primeras semillas (Finales de los 80s &ndash; 90s)</p>
      <p align="justify" class="Estilo4" data-start="365" data-end="541">Aunque el t&eacute;rmino &ldquo;indie&rdquo; a&uacute;n no era com&uacute;n en M&eacute;xico, surgieron bandas fuera del circuito comercial que empezaron a explorar sonidos distintos al rock en espa&ntilde;ol dominante.</p>
      <p align="justify" class="Estilo5" data-start="543" data-end="566"> Bandas pioneras:</p>
      <div align="justify">
        <ul class="Estilo4" data-start="567" data-end="907">
          <li data-start="567" data-end="700"><strong>Caifanes, Santa Sabina, La Lupita: </strong>Si bien m&aacute;s cercanas al rock alternativo, marcaron una independencia creativa clave.</li>
          <li data-start="701" data-end="780"><strong>La Barranca:</strong> Rock experimental, letras profundas, est&eacute;tica introspectiva.</li>
          <li data-start="781" data-end="907"><strong>Austin TV (formados en 2001, pero influenciados por esta etapa): </strong>Representan el &ldquo;hazlo t&uacute; mismo&rdquo; en su m&aacute;xima expresi&oacute;n.</li>
        </ul>
      </div>
      <p align="justify" class="Estilo5" data-start="914" data-end="982"> 2. El movimiento &quot;Happy-Fi&quot; y el indie del norte (2000s)</p>
      <p align="justify" class="Estilo4" data-start="983" data-end="1099">Durante esta d&eacute;cada se consolidan sellos, colectivos y festivales que dan forma a una escena 100% independiente.</p>
      <p align="justify" class="Estilo5" data-start="1101" data-end="1150"> Movimiento clave: Happy-Fi (Monterrey)      </p>
      <p align="justify" class="Estilo4" data-start="1101" data-end="1150">Colectivo y sello que agrup&oacute; a bandas como: </p>
      <div align="justify">
        <ul class="Estilo4" data-start="1151" data-end="1278">
          <li><strong>Ni&ntilde;a </strong></li>
          <li><strong>Banda de Turistas</strong></li>
          <li><strong>Los Dynamite</strong></li>
          <li><strong>She's a Tea</strong></li>
        </ul>
      </div>
      <p align="justify"><strong>Caracter&iacute;sticas:</strong>      </p>
      <div align="justify">
        <ul class="Estilo4" data-start="1304" data-end="1500">
          <li data-start="1304" data-end="1358"><strong>DIY total: </strong>producci&oacute;n, distribuci&oacute;n, dise&ntilde;o gr&aacute;fico</li>
          <li>Sonido entre el indie rock, electro y post-punk revival.</li>
          <li data-start="1418" data-end="1500">Monterrey se convierte en un epicentro alternativo junto con Guadalajara y CDMX.</li>
